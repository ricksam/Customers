/*SCRIPT SERVER PHP TABLE*/
CREATE TABLE MARCELO_VENDAS(
  ID INTEGER NOT NULL AUTO_INCREMENT,
  EMPRESA VARCHAR(60),
  CNPJ VARCHAR(20),
  EMISSAO DATE,
  INICIO VARCHAR(8),
  COD_OPERADOR INTEGER,
  OPERADOR VARCHAR(60),
  CUPONS INTEGER,
  TOTAL NUMERIC(15,4),
  CONSTRAINT PK_VENDAS PRIMARY KEY(ID)
);

CREATE TABLE RELATORIO
(
  ID INTEGER NOT NULL AUTO_INCREMENT,
  HTML_CODE TEXT,
  CHAVE_ACESSO VARCHAR(200),
  CONSTRAINT PK_RELATORIO PRIMARY KEY(ID) 
);

CREATE TABLE MARCELO_QTDE_VENDAS(
  ID INTEGER NOT NULL AUTO_INCREMENT,
  EMPRESA VARCHAR(60),
  CNPJ VARCHAR(20),
  EMISSAO DATE,
  ID_MENOR INTEGER,
  ID_MAIOR INTEGER,
  CONTAGEM INTEGER,
  DIFERENCA INTEGER,
  CONSTRAINT PK_QTDE_VENDAS PRIMARY KEY(ID)
);

CREATE TABLE SQL_LOG(
  ID INTEGER NOT NULL AUTO_INCREMENT,
  DESCRICAO TEXT,
  CONSTRAINT PK_QTDE_VENDAS PRIMARY KEY(ID)
);